package com.Sport_Project001.dto;

public class RankDto {
	private String rank;
	private String club;
	private String played;
	private String points;
	private String goaldif;
	private String goallose;
	private String goalscore;
	private String win;
	private String draw;
	private String lose;
	private String clubimg;
	
	
	
	public String getClubimg() {
		return clubimg;
	}
	public void setClubimg(String clubimg) {
		this.clubimg = clubimg;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getClub() {
		return club;
	}
	public void setClub(String club) {
		this.club = club;
	}
	public String getPlayed() {
		return played;
	}
	public void setPlayed(String played) {
		this.played = played;
	}
	public String getPoints() {
		return points;
	}
	public void setPoints(String points) {
		this.points = points;
	}
	public String getGoaldif() {
		return goaldif;
	}
	public void setGoaldif(String goaldif) {
		this.goaldif = goaldif;
	}
	public String getGoallose() {
		return goallose;
	}
	public void setGoallose(String goallose) {
		this.goallose = goallose;
	}
	public String getGoalscore() {
		return goalscore;
	}
	public void setGoalscore(String goalscore) {
		this.goalscore = goalscore;
	}
	public String getWin() {
		return win;
	}
	public void setWin(String win) {
		this.win = win;
	}
	public String getDraw() {
		return draw;
	}
	public void setDraw(String draw) {
		this.draw = draw;
	}
	public String getLose() {
		return lose;
	}
	public void setLose(String lose) {
		this.lose = lose;
	}
	
	
}
